function getLang() {
  return localStorage.getItem('report_lang') || 'zh';
}

function setLang(lang) {
  localStorage.setItem('report_lang', lang);
  applyI18n();
}

function t(key) {
  const lang = getLang();
  return I18N_DICT[lang][key] || I18N_DICT['zh'][key] || key;
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.type === 'button' || el.type === 'submit') {
        el.value = t(key);
      } else {
        el.placeholder = t(key);
      }
    } else {
      el.textContent = t(key);
    }
  });

  // Apply titles if they have data-i18n-title
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });

  // Apply page title
  document.title = t('site.title');

  // Trigger language change events
  const event = new CustomEvent('langChanged', { detail: { lang: getLang() } });
  document.dispatchEvent(event);
}

// Automatically apply i18n when page content loads
document.addEventListener('DOMContentLoaded', () => {
  applyI18n();
});
